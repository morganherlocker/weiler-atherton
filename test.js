var test = require('tape')
var clipper = require('./')

test('clip', function(t){
	var subject = [
            [
              -72.94921875,
              28.844673680771795
            ],
            [
              -72.94921875,
              38.47939467327645
            ],
            [
              -58.88671875,
              38.47939467327645
            ],
            [
              -58.88671875,
              28.844673680771795
            ],
            [
              -72.94921875,
              28.844673680771795
            ]
          ]

	var clip = [
				[
	              -77.9150390625,
	              34.125447565116126
	            ],
	            [
	              -77.9150390625,
	              42.06560675405716
	            ],
	            [
	              -65.8740234375,
	              42.06560675405716
	            ],
	            [
	              -65.8740234375,
	              34.125447565116126
	            ],
	            [
	              -77.9150390625,
	              34.125447565116126
	            ]
	        ]


	var clipped = clipper(subject, clip)
	t.ok(clipped)
	t.notEqual(clipped.length, 0)
	t.end()
});